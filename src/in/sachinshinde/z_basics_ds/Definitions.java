package in.sachinshinde.z_basics_ds;

public class Definitions {

	/*
	 	----------------------
	 	1. Full binary tree 
	 		It is a binary tree where each node has exactly 0 or 2 children.
	  	----------------------
	  	2. Height-balanced binary tree
			a binary tree in which the left and right subtrees 
			of every node differ in height by no more than 1
		----------------------	
		3. Diameter of a binary tree 
			It is the length of the longest path between any two nodes in a tree.
			The length of a path between two nodes is represented by 
				the number of edges between them.
 			This path may or may not pass through the root.
 		----------------------
 		4. An inversion, or mirror, of a Binary Tree (T):
 			It is just a Binary Tree M(T) whose left and right children 
 			(of all non-leaf nodes) are swapped.
 		----------------------
 		5. Size of a tree:
 			It is the total number of elements present in the tree
 		----------------------
 		6. Maximum width of a tree 
 			It is the maximum width among all levels.
 			The width of one level is defined as the length between the end-nodes 
			(the leftmost and rightmost non-null nodes), where 
			the null nodes between the end-nodes that would be present in a complete binary tree 
			extending down to that level are also counted into the length calculation.
		----------------------
		7. Ceil Value Node in BST: 
		   	A Node with the smallest data larger than or equal to the key value.
		----------------------
		8. Cost of a BST Node:
			The level of that node multiplied by its frequency. The level of the root is 1.
		----------------------
	 */
}
